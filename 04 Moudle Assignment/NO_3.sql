Data: 
1.Data is unorganised and unrefined facts.
2.Data is an individual unit that contains raw materials which do not carry any sepcific meaning.
3.Data does not depend on informtaion.
4.Row data alone is insufficients for decision making.
5.An example of data is a student test score.

Information : 
1.Information comprises processed, organised data presented in a meaningful context.
2.Information is a group of data that collectively carries a logical meaning.
3.Information depends on data.
4.Information is sufficient for decision making
5.The average score of a class is the information derived from the given data.


