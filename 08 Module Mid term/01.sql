-- Write the difference between Primary Key and Composite Primary Key

PRIMARY KEY : 

1. A primary key constraint uniquely identifies each record in a table .
2.It must contain unique values and cannot have NULL values.
3.There can only be one primary key in a table.



COMPOSITE KEY :

1. A compostite key consists of two or more columns that together uniquely identify each row/record in a tabel.
2.Unlike a primary key, indiviual columns in a compostite key may not be unique on their own, but their combination  is unique.
3.compostitekey are useful when a single column cannot uniquely identify a row , but a combination of columns can.