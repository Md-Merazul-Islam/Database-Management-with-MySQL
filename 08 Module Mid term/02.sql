-- Write the difference between using JOIN Query and not using JOIN query

Using JOIN Query: 

1. Efficien retrieves related data from multiple tables in a single Query.
2.Improves perfomance by leveraging database indexes and execution plans.
3.Ensure data consistency by retrieving related data together.



Not Using JOIN Query:

1. Requires fetching data from each table separately and combining it afterward.
2.May lead to perfomance issues,especially with large datasets or complex relationships.
3.Results in longer and less intuitive code,making it harder to understand and maintain.